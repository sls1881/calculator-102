//Math for addition
export function getSum(addNum1, addNum2) {
    return addNum1 + addNum2;
}

//Math for subtraction
export function getSub(subNum1, subNum2) {
    return subNum1 - subNum2;
}

//Math for multiplication
export function getMult(multNum1, multNum2) {
    return multNum1 * multNum2;
}

//Math for division
export function getDiv(divNum1, divNum2) {
    return divNum1 / divNum2;
}